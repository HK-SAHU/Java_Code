public class assignmentoperator {
    public static void main(String[] args) {
        int a=5;
        a+=5;
        System.out.println(a);
        a-=5;
        System.out.println(a);
        a/=5;
        System.out.println(a);
        a*=5;
        System.out.println(a);
        a%=5;
        System.out.println(a);

        int m1,m2,m3,m4;
        m1=m2=m3=m4=10;
        System.out.println(m1+" "+m2+" "+m3+" "+m4);
    }
}
